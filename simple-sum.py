# LC 2235 - Add Two Integers
# https://leetcode.com/problems/add-two-integers/
# Time: O(1), Space: O(1)

class Solution(object):
    def sum(self, num1, num2):
        """
        Returns the sum of two integers.

        :type num1: int
        :type num2: int
        :rtype: int
        """
        return num1 + num2
